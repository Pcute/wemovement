package domain;

public class Classification {
	private int claId;
	private String claNo;
	private String claName;
	private int creditsId;
	private int columeId;

	public int getClaId() {
		return claId;
	}

	public void setClaId(int claId) {
		this.claId = claId;
	}

	public String getClaNo() {
		return claNo;
	}

	public void setClaNo(String claNo) {
		this.claNo = claNo;
	}

	public String getClaName() {
		return claName;
	}

	public void setClaName(String claName) {
		this.claName = claName;
	}

	public int getCreditsId() {
		return creditsId;
	}

	public void setCreditsId(int creditsId) {
		this.creditsId = creditsId;
	}

	public int getColumeId() {
		return columeId;
	}

	public void setColumeId(int columeId) {
		this.columeId = columeId;
	}
}
